const KEYWORDS: { mood: Mood; words: string[] }[] = [
  { mood: "dump", words: ["던져", "버려", "다 싫", "포기", "때려치", "갈아엎", "다 관둬"] },
  { mood: "rebel", words: ["하기 싫", "하기싫", "귀찮", "못하겠", "안 할래", "안할래", "도망", "쉬고", "놀고", "눕고"] },
  { mood: "excuse", words: ["핑계", "빠지", "땡땡이", "변명"] },
  { mood: "roast", words: ["팩폭", "잔소리", "혼내", "따끔", "정신", "현실"] },
];

export function guessMood(text: string): Mood {
  const t = text.toLowerCase();
  for (const k of KEYWORDS) if (k.words.some((w) => t.includes(w))) return k.mood;
  return "default";
}

export function pickLine(mood: Mood): string {
  const arr = LINES[mood] ?? LINES.default;
  return arr[Math.floor(Math.random() * arr.length)];
}
